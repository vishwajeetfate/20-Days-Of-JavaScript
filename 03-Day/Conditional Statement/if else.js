// check if the value if positive, negative or zero
const value = prompt("Enter a another number for If else code: ");


// check if value is greater than 0
if (value > 0) {
   document.write("The value is positive");
}
// check if number is 0
else if (value == 0) {
  document.write("The value is 0");
}
// if value is neither greater than 0, nor zero
else {
    document.write("The value is negative");
}
